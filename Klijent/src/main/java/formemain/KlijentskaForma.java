    private void proveriPopust() {
        if (txtUkupnaCena.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Morate prvo ubaciti stavku!");
            return;
        }
        Kupac k = (Kupac) cmbKupac.getSelectedItem();
        int[] nizBrojeva = new int[101];
        int j = 0;
        for (int i = 0; i <= 1000; i += 10) {
            nizBrojeva[j] = i;
            j++;
        }
        double popust = 0;
        for (int i = 1; i < nizBrojeva.length; i++) {
            if (k.getPoeni() >= nizBrojeva[i] && k.getPoeni() <= nizBrojeva[i + 1]) {
                popust = (nizBrojeva[i] + nizBrojeva[i + 1]) / 10;
                JOptionPane.showMessageDialog(this, "Kupac ima pravo na " + popust + " % popusta!");
                break;
            }
        }
        if (popust == 0) {
            JOptionPane.showMessageDialog(this, "Kupac nema pravo na popust!");
        }

        txtPopust.setText(popust + "");
        double cena = Double.parseDouble(txtUkupnaCena.getText());
        double obracunatPopust = cena - ((cena * (popust / 100)));
        txtUkupnaCena.setText(obracunatPopust + "");
    }
