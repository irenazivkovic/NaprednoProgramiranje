    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        try {
            if (txtPopust.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Obracunajte popust!");
                return;
            }

            ModelTabeleStavke mt = (ModelTabeleStavke) tblStavke.getModel();

            Narudzbenica n = new Narudzbenica(-1, sdf.parse(txtDatum.getText()), Double.parseDouble(txtPopust.getText()), Double.parseDouble(txtUkupnaCena.getText()), Status.KREIRANA, (Kupac) cmbKupac.getSelectedItem(), mt.getLista(), sesija.Sesija.getInstance().getUlogovani());
            boolean uspesno = KlijentKontroler.getInstance().addNarudzbenica(n);
            if (uspesno) {
                JOptionPane.showMessageDialog(this, "Uspesno sacuvana narudzbenica!");
                for (Stavka stavka : mt.getLista()) {
                    int stanjePre = stavka.getKnjiga().getStanje();
                    stavka.getKnjiga().setStanje(stanjePre - stavka.getKolicina());
                    KlijentKontroler.getInstance().updateKnjiga(stavka.getKnjiga());
                }
                Kupac k = (Kupac) cmbKupac.getSelectedItem();
                k.setPoeni(k.getPoeni() + 10);
                KlijentKontroler.getInstance().updateKupac(k);
                new KlijentskaForma().setVisible(true);
                this.dispose();

            } else {
                JOptionPane.showMessageDialog(this, "Doslo je do greske...");
            }

        } catch (Exception ex) {
            Logger.getLogger(KlijentskaForma.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int row = tblStavke.getSelectedRow();
        if (row >= 0) {
            ModelTabeleStavke mt = (ModelTabeleStavke) tblStavke.getModel();
            mt.obrisi(row);
            double cena = mt.vratiUkupnuCenu();
            txtUkupnaCena.setText(cena + "");
            ukupnaCena=cena;
        } else {
            JOptionPane.showMessageDialog(this, "Odaberite stavku!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

