    void dodaj(Stavka s) {
        ModelTabeleStavke mt = (ModelTabeleStavke) tblStavke.getModel();
        mt.dodaj(s);
        JOptionPane.showMessageDialog(this, "Na cenu dodato " + s.getPDV() + " % PDV-a!");
        ukupnaCena += (s.getKolicina() * s.getKnjiga().getCena()) + (((s.getKolicina() * s.getKnjiga().getCena()) * s.getPDV()) / 100);
        txtUkupnaCena.setText(ukupnaCena + "");
    }

